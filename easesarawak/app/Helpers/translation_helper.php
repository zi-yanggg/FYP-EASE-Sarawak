if (! function_exists('normalize_site_locale')) {
    function normalize_site_locale(?string $locale): string
    {
        $locale = strtolower(trim((string) $locale));

        return match ($locale) {
            'ms', 'bm', 'my', 'bahasa', 'bahasa-melayu' => 'ms',
            'zh', 'cn', 'zh-cn', 'zh-hans', '中文', 'chinese' => 'zh',
            default => 'en',
        };
    }
}

if (! function_exists('ease_current_lang')) {
    function ease_current_lang(): string
    {
        $requestLang = $_GET['lang'] ?? null;
        $sessionLang = function_exists('session') ? session()->get('site_lang') : null;
        $cookieLang  = $_COOKIE['site_lang'] ?? null;
        $request     = function_exists('service') ? service('request') : null;
        $requestLocale = ($request && method_exists($request, 'getLocale')) ? $request->getLocale() : null;

        return normalize_site_locale($requestLang ?: $sessionLang ?: $cookieLang ?: $requestLocale ?: 'en');
    }
}

if (! function_exists('ease_translate')) {
    function ease_translate(string $text, ?string $lang = null): string
    {
        $lang = normalize_site_locale($lang ?: ease_current_lang());
        $catalog = ease_translation_catalog();

        return $catalog[$lang][$text] ?? $text;
    }
}

